// Map social link key to icon component
export const socialIconMap = {
  facebook: FacebookIcon,
  instagram: InstagramIcon,
  twitter: TwitterIcon,
  tiktok: TikTokIcon,
  youtube: YouTubeIcon,
  whatsapp: WhatsAppIcon,
};

// Render social links from config
export function SocialLinks({ links, size = 18, className = '' }) {
  if (!links || !Object.values(links).some(Boolean)) return null;
  return (
    <div className={`flex items-center gap-3 ${className}`}>
      {Object.entries(links).map(([key, url]) => {
        if (!url || key === 'whatsapp') return null;
        const Icon = socialIconMap[key];
        if (!Icon) return null;
        return (
          <a key={key} href={url} target="_blank" rel="noopener noreferrer"
            className="opacity-60 hover:opacity-100 transition" title={key.charAt(0).toUpperCase() + key.slice(1)}>
            <Icon size={size} />
          </a>
        );
      })}
    </div>
  );
}

